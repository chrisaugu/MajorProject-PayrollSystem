    private void jButtonViewPayCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonViewPayCheckActionPerformed
        // TODO add your handling code here:
        double tax = Double.parseDouble(jTextFieldTaxMonthlyWageEmpDetail.getText());
        
        if (employee.getPayCode() == MANAGERS_PAYCODE) {
            jTextFieldRatePerHourEmpDetail.setText(String.format("%.2f", 11.38));
            double ratePerHour = Double.parseDouble(jTextFieldRatePerHourEmpDetail.getText());
            grossSalary = ratePerHour * 8 * 14;
            double deduc = (tax/(double)100) * grossSalary;
            double salary = 250 + (5.7/(double)100) * grossSalary;
            double netSalary = salary - deduc;
            
            jTextFieldEmpDetailGrossSalary.setText(String.format("%.2f", salary));
            jTextFieldEmpDetailDeduction.setText(String.format("%.2f", deduc));
            jTextFieldEmpDetailNetSalary.setText(String.format("%.2f", netSalary));            
        }
        // if employee is a Hourly worker
        else if (employee.getPayCode() == HOURLY_WORKERS_PAYCODE) {
            double ratePerHour = Double.parseDouble(jTextFieldRatePerHourEmpDetail.getText());
//            double hoursWorked = Double.parseDouble(jTextFieldTotalHoursWorkedEmpDetail.getText());
//            grossSalary += ratePerHour * hoursWorked;
            double deduc = (tax/(double)100) * grossSalary;
            double netSalary = grossSalary - deduc;
            
            jTextFieldEmpDetailGrossSalary.setText(String.format("%.2f", grossSalary));
            jTextFieldEmpDetailDeduction.setText(String.format("%.2f", deduc));
            jTextFieldEmpDetailNetSalary.setText(String.format("%.2f", netSalary));
        }
        else if (employee.getPayCode() == COMMISSION_WORKERS_PAYCODE) {
            double sales = Double.parseDouble(jTextFieldCommissionSalesEmpDetail.getText());
            double deduc = (tax/(double)100) * sales;
            double salary = 250 + (5.7/(double)100) * sales;
            double netSalary = salary - deduc;
            
            jTextFieldEmpDetailGrossSalary.setText(String.format("%.2f", salary));
            jTextFieldEmpDetailDeduction.setText(String.format("%.2f", deduc));
            jTextFieldEmpDetailNetSalary.setText(String.format("%.2f", netSalary));
        }
    }//GEN-LAST:event_jButtonViewPayCheckActionPerformed

