    private void jButtonSaveEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveEmployeeActionPerformed
        // TODO add your handling code here:
//        Object selectedItem = jComboBoxEmpTypeEmpEntry.getSelectedItem();
        int selectedIndex = jComboBoxEmpTypeEmpEntry.getSelectedIndex();
        switch (selectedIndex+1) {
            case MANAGERS_PAYCODE:
                employee.setPayCode(MANAGERS_PAYCODE);
                break;
            case HOURLY_WORKERS_PAYCODE:
                employee.setPayCode(HOURLY_WORKERS_PAYCODE);
                break;
            case COMMISSION_WORKERS_PAYCODE:
                employee.setPayCode(COMMISSION_WORKERS_PAYCODE);
                break;
        }
        employee.setName(jTextFieldEmpNameEmpEntry.getText());
        if (employee.getPayCode() == 1) {
            String[] rec = { employee.getName(), String.format("%d", employee.getPayCode()), "Manager"};
            model.addRow(rec);
        }
        else if (employee.getPayCode() == 2) {
            String[] rec = { employee.getName(), String.format("%d", employee.getPayCode()), "Hourly Worker"};
            model.addRow(rec);
        }
        else if (employee.getPayCode() == 3) {
            String[] rec = { employee.getName(), String.format("%d", employee.getPayCode()), "Commission Worker"};
            model.addRow(rec);
        }
        jDialogEmployeeEntry.dispose();
    }//GEN-LAST:event_jButtonSaveEmployeeActionPerformed

