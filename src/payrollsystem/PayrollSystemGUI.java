    private void jCheckBoxSaturdayEmpDetailFirstWkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBoxSaturdayEmpDetailFirstWkActionPerformed
        // TODO add your handling code here:
        double hourlyRate = Double.parseDouble(jTextFieldRatePerHourEmpDetail.getText());
        if (hourlyRate > 0) {
            if (jCheckBoxSaturdayEmpDetailFirstWk.isSelected()) {
                daysWorked++;
                grossSalary += 1.5 * hourlyRate * 8;
            } else {
                daysWorked--;
                grossSalary -= 1.5 * hourlyRate * 8;
            }
            hoursWorked = daysWorked * 8;
            jTextFieldTotalHoursWorkedEmpDetail.setText(String.format("%d", hoursWorked));
            jTextFieldDaysWorkedEmpDetail.setText(String.format("%d", daysWorked));
        } 
        else {
            JOptionPane.showMessageDialog(null, 
                    "No hourly rate", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jCheckBoxSaturdayEmpDetailFirstWkActionPerformed

